// switch statement

let day = 13;

switch (day) {
  case 0:
    console.log("Sunday");
    break;
  case 2:
    console.log("Monday");
    break;
  case 3:
    console.log("Tuesday");
    break;
  case 4:
    console.log("Wednesday");
    break;
  case 5:
    console.log("Thursday");
    break;
  case 5:
    console.log("friday");
    break;
  default:
    console.log("invalid input");
}
